# Input: รับจำนวนเต็ม 3 จำนวนจากแป้นพิมพ์ (บรรทัดละจำนวน) เก็บในตัวแปร h, m และ s ซึ่งแทนจำนวน ชั่วโมง นาที และ วินาที
# Process: คำนวณจำนวนวินาทีรวมที่คิดจาก h, m และ s
# Output: จำนวนวินาทีรวมทั้งหมดที่คำนวณได้

h = int(input())
m = int(input())
s = int(input())
# 1m = 60s , 1h = 60m , 60*60 s = 1h
